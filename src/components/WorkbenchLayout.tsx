'use client'

import { useState } from 'react'
import dynamic from 'next/dynamic'
import ChatInterface from '@/components/ChatInterface'

const MonacoEditor = dynamic(() => import('@monaco-editor/react'), { ssr: false })

export default function WorkbenchLayout() {
  const [prompt, setPrompt] = useState('')
  const [analysis, setAnalysis] = useState('')
  const [stack, setStack] = useState({ frontend: '', backend: '' })
  const [tasks, setTasks] = useState({ frontend: '', backend: '' })
  const [frontendCode, setFrontendCode] = useState('')
  const [backendCode, setBackendCode] = useState('')
  const [logs, setLogs] = useState<string[]>([])
  const [loading, setLoading] = useState(false)

  const appendLog = (msg: string) => setLogs((l) => [...l, msg])

  const handleSubmit = async () => {
    setLoading(true)
    appendLog('🧠 Submitting prompt to manager agent...')

    const res = await fetch('/api/generate', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ prompt }),
    })

    const data = await res.json()
    setAnalysis(data.analysis || '')
    setStack(data.stack || { frontend: '', backend: '' })
    setTasks(data.tasks || { frontend: '', backend: '' })
    setFrontendCode(data.frontend || '')
    setBackendCode(data.backend || '')
    appendLog('✅ Code generated by frontend/backend agents.')
    setLoading(false)
  }

  return (
    <div className="flex h-screen">
      {/* Left: Prompt + Agent Chat */}
      <div className="w-1/3 p-4 border-r border-gray-300 overflow-y-auto">
        <h1 className="text-2xl font-bold mb-4">🧠 Vibe Forge</h1>

        <textarea
          value={prompt}
          onChange={(e) => setPrompt(e.target.value)}
          className="w-full h-24 p-2 border rounded mb-4"
          placeholder="Describe what you want to build..."
        />

        <button
          onClick={handleSubmit}
          className="w-full px-4 py-2 bg-black text-white rounded mb-6"
          disabled={loading}
        >
          {loading ? 'Building...' : '🧠 Forge'}
        </button>

        <ChatInterface
          agent="manager"
          setAnalysis={setAnalysis}
          setStack={setStack}
          setTasks={setTasks}
          setFrontendCode={setFrontendCode}
          setBackendCode={setBackendCode}
        />
        <ChatInterface
          agent="frontend"
          onCodeUpdate={(code) => setFrontendCode(code)}
        />
        <ChatInterface
          agent="backend"
          onCodeUpdate={(code) => setBackendCode(code)}
        />
      </div>

      {/* Right: Code Editors + Logs */}
      <div className="flex-1 flex flex-col">
        <div className="flex-1 grid grid-cols-2 gap-4 p-4">
          <div>
            <h2 className="font-semibold mb-1">🎨 Frontend</h2>
            <MonacoEditor
              height="calc(100vh - 180px)"
              defaultLanguage="typescript"
              value={frontendCode}
              options={{ readOnly: true }}
            />
          </div>
          <div>
            <h2 className="font-semibold mb-1">🛠 Backend</h2>
            <MonacoEditor
              height="calc(100vh - 180px)"
              defaultLanguage="typescript"
              value={backendCode}
              options={{ readOnly: true }}
            />
          </div>
        </div>

        {/* Logs */}
        <div className="bg-gray-100 p-2 h-40 overflow-y-auto text-sm border-t border-gray-300">
          {logs.map((log, idx) => (
            <div key={idx}>• {log}</div>
          ))}
        </div>
      </div>
    </div>
  )
}
